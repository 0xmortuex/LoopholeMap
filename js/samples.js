const SAMPLE_REGULATION = `REGULATION ON DIGITAL ADVERTISING PRACTICES

Issued by the Federal Communications and Commerce Authority

ARTICLE 1. PURPOSE AND SCOPE.
(1) This regulation governs digital advertising practices for entities operating within the jurisdiction, with the purpose of protecting consumers from deceptive, manipulative, or intrusive advertising techniques.
(2) This regulation applies to all digital advertisers, advertising platforms, and intermediaries with annual digital advertising revenue exceeding $500,000.

ARTICLE 2. DEFINITIONS.
(1) "Digital advertisement" means any paid promotional content delivered through electronic means, including but not limited to display ads, sponsored content, video pre-rolls, and social media promotions.
(2) "Behavioral targeting" means the practice of collecting and analyzing user behavior data to deliver personalized advertising content.
(3) "Dark pattern" means a user interface design choice that manipulates users into taking actions they did not intend.
(4) "Advertising platform" means any service that facilitates the placement, delivery, or measurement of digital advertisements on behalf of third parties.

ARTICLE 3. DISCLOSURE REQUIREMENTS.
(1) All digital advertisements must be clearly and conspicuously labeled as advertising or sponsored content.
(2) Native advertisements that resemble editorial content must include a prominent disclosure visible without scrolling or user interaction.
(3) Influencer endorsements must disclose the material connection between the endorser and the advertiser "where practicable."

ARTICLE 4. BEHAVIORAL TARGETING RESTRICTIONS.
(1) Entities engaging in behavioral targeting must obtain consent from users prior to collecting behavioral data for advertising purposes.
(2) Users must be provided with a "reasonable mechanism" to opt out of behavioral targeting.
(3) Behavioral targeting of individuals known to be under 16 is prohibited, except where the advertising content is deemed "age-appropriate" by the advertiser.
(4) Sensitive behavioral categories including health conditions, financial distress, and political affiliation shall not be used for targeting "unless necessary for the delivery of relevant services."

ARTICLE 5. PROHIBITED PRACTICES.
(1) No entity may employ dark patterns in connection with advertising consent interfaces.
(2) Countdown timers, false urgency indicators, and manufactured scarcity claims are prohibited unless they reflect genuine time-limited offers.
(3) Advertisements must not contain claims that are "materially misleading" to a "reasonable consumer."
(4) Retargeting of users who have explicitly opted out of tracking is prohibited, except through "contextual advertising methods."

ARTICLE 6. DATA RETENTION AND SHARING.
(1) Advertising-related user data may be retained for a period not exceeding 24 months from the date of collection.
(2) Transfer of advertising data to third parties requires user notification, which may be provided through a general privacy policy update.
(3) Anonymized or aggregated advertising data is exempt from the restrictions of this article.

ARTICLE 7. ENFORCEMENT AND PENALTIES.
(1) The Authority shall investigate complaints and may conduct audits of entities subject to this regulation.
(2) Violations are subject to fines of up to $10,000 per incident. For the purposes of this article, an "incident" is defined as a single campaign or advertising initiative found to be in violation.
(3) Entities that self-report violations within 30 days of discovery shall receive a reduction of up to 50% in applicable fines.
(4) No private right of action is created by this regulation. Enforcement is exclusively through the Authority.

ARTICLE 8. SAFE HARBOR.
(1) Entities that implement an advertising compliance program approved by the Authority shall be afforded a safe harbor from penalties for first-time violations, provided the violation is corrected within 60 days of notification.
(2) The Authority shall publish guidelines for approved compliance programs within 12 months of this regulation taking effect.

ARTICLE 9. EFFECTIVE DATE.
This regulation shall take effect 90 days after publication. Entities with annual revenue below $2,000,000 shall have an additional 180-day grace period.`;

